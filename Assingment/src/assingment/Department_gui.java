    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String dcode_txt = dcode.getText();
        String name_txt = txtname.getText();
        String description = txtdes.getText();
        if (dcode_txt.isEmpty() || name_txt.isEmpty() || description.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields.", "Warning", JOptionPane.WARNING_MESSAGE);
     
        }else{
            Department dpt = new Department(0,  dcode_txt,  name_txt,  description);
            Department_service dpt_service = new Department_service();
            boolean save_status = dpt_service.createDPT(dpt);
            if (save_status) {
                 fun_desable_update_delete();
                 JOptionPane.showMessageDialog(null, "Department is saved", "Save Successful", JOptionPane.INFORMATION_MESSAGE);
                 clearfun();
                 view_table();
                 random_fun();
            }else{
                 JOptionPane.showMessageDialog(null, "Data saving failed.", "Save Failed", JOptionPane.ERROR_MESSAGE);

            } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public void view_table() {
        Department_service dpt_service = new Department_service();
        List<Department> departments = dpt_service.retrieveAllDepartments();
    
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("DCode");
        model.addColumn("Name");
        model.addColumn("Description");
    
        for (Department department : departments) {
            model.addRow(new Object[]{
                department.getId(),
                department.getDcode(),
                department.getName(),
                department.getDescription(),
            });
        }

        departmentTable.setModel(model);
    }
    
