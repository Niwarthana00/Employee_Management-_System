    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        if (choice == JOptionPane.YES_OPTION) {
            
            Department dpt = new Department();
            dpt.setId(id);
            
            Department_service dpt_service = new Department_service();
            boolean delete_status = dpt_service.deleteDPT(id);
            if (delete_status) {
                view_table();
                fun_desable_update_delete();
                clearfun();
                random_fun();
                JOptionPane.showMessageDialog(null, "Department deleted successfully", "Delete Status", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete department", "Delete Status", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cancel deleting.", "Cancel Delete", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            String dcode_txt = dcode.getText();
            String name_txt = txtname.getText();
            String description = txtdes.getText();
            if (dcode_txt.isEmpty() || name_txt.isEmpty() || description.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill in all fields.", "Warning", JOptionPane.WARNING_MESSAGE);

            }else{
                    Department dpt = new Department(id , dcode_txt , name_txt ,description );
                    Department_service dpt_service = new Department_service();
                    boolean status = dpt_service.updateDPT(id, dpt);
                    if (status) {
                        view_table();
                        fun_desable_update_delete();
                        clearfun();
                        random_fun();
                        
                        JOptionPane.showMessageDialog(null, "Department updated successfully", "Update Status", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to update department", "Update Status", JOptionPane.ERROR_MESSAGE);
                    }
            }  
        }else{
            JOptionPane.showMessageDialog(null, "Cancel updating.", "Cancel Update", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void departmentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_departmentTableMouseClicked
        int selectedRowIndex = departmentTable.getSelectedRow();
        if (selectedRowIndex != -1) { 
            
            int id_txt = (int) departmentTable.getValueAt(selectedRowIndex, 0);
            Object dcode_txt = departmentTable.getValueAt(selectedRowIndex, 1);
            Object name_txt = departmentTable.getValueAt(selectedRowIndex, 2);
            Object description_txt = departmentTable.getValueAt(selectedRowIndex, 3);
           
            fun_enable_update_delete();
            
            id=id_txt;
            dcode.setText((String) dcode_txt);
            txtname.setText((String) name_txt);
            txtdes.setText((String) description_txt);
            
        } else {
            JOptionPane.showMessageDialog(null, "No data from this id","No Data" , JOptionPane.WARNING_MESSAGE);

           
        }

    }//GEN-LAST:event_departmentTableMouseClicked

