    private void view_table() {
        Hr_Manager_service hr_service = new Hr_Manager_service();
        List<HR_Manager> hrmanager = hr_service.retrieveAllHRMANAGER();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Name");
        model.addColumn("Email");
        model.addColumn("BOD");
        model.addColumn("Education Qualification");
        model.addColumn("NIC");
        model.addColumn("Telephone Number");
        model.addColumn("Address");
        model.addColumn("Joined Date");


        for (HR_Manager hrman : hrmanager) {
            model.addRow(new Object[]{
                hrman.getId(),
                hrman.getName(),
                hrman.getEmail(),
                hrman.getBod(), 
                hrman.getEducation_qualification(),
                hrman.getNic(),
                hrman.getTelephoneNumber(),
                hrman.getAddress(),
                hrman.getTimestamp()
            });
        }

        hrtable.setModel(model);
    }

