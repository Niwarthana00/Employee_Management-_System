    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
 
        User_service user_service = new User_service();
        String name_txt = txtName.getText();
        String add_txt = txtAddress.getText();
        String nic_txt = txtNIC.getText();
        String dob_txt = txtBod.getText();
        String email_txt = txtEmail.getText();
        String passs = user_service.password_generate();
        String eq_txt = txtEducationQualification.getText();
        String tel = txtTel.getText();
        
        boolean isValid = true;
        StringBuilder errorMessage = new StringBuilder();
        
        if (!validation.isValidNIC(nic_txt)) {
            isValid = false;
            errorMessage.append("Please enter a valid NIC number.\n");
        }
        
        if (!validation.isValidEmail(email_txt)) {
            isValid = false;
            errorMessage.append("Please enter a valid email address.\n");
        }
        
        if (!validation.isValidPhoneNumber(tel)) {
            isValid = false;
            errorMessage.append("Please enter a valid telephone number.\n");
        }
        
       
        
        if (add_txt.isEmpty() || nic_txt.isEmpty() || dob_txt.isEmpty() || email_txt.isEmpty() || eq_txt.isEmpty()) {
            isValid = false;
            errorMessage.append("Please fill in all required fields.\n");
        }
        
        if (!isValid) {
            JOptionPane.showMessageDialog(this, errorMessage.toString(), "Error", JOptionPane.WARNING_MESSAGE);
        }else{
        
            HR_Manager hr = new HR_Manager (name_txt , add_txt , nic_txt , dob_txt , eq_txt , 0 , email_txt , passs , "HR Manager" , tel, "2014-02-10");
            Hr_Manager_service hr_service = new Hr_Manager_service();
            boolean save_status = hr_service.createUser(hr);
            if (save_status) {
                fun_desable_update_delete();
                JOptionPane.showMessageDialog(null, "HR Manager is saved. Passowrd Is : "+passs, "Save Successful", JOptionPane.INFORMATION_MESSAGE);
                clearfun();
                view_table();
                
                 User_service usr_service = new User_service();
                 usr_service.download_password(passs , email_txt);
            }else{
                JOptionPane.showMessageDialog(null, "Data saving failed.", "Save Failed", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {

            HR_Manager hrm = new HR_Manager();
            hrm.setId(id);

            Hr_Manager_service hr_m_service = new Hr_Manager_service();
            boolean delete_status = hr_m_service.deleteUser(id , email_for_delete);
            if (delete_status) {
                view_table();
                fun_desable_update_delete();
                clearfun();
                JOptionPane.showMessageDialog(null, "HR Manager deleted successfully", "Delete Status", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete HR Manager", "Delete Status", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cancel deleting.", "Cancel Delete", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int choice = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?", "Confirmation", JOptionPane.YES_NO_OPTION);

        if (choice == JOptionPane.YES_OPTION) {
            String name_txt = txtName.getText();
            String add_txt = txtAddress.getText();
            String eq_txt = txtEducationQualification.getText();
            String passv= pass;



            if (add_txt.isEmpty() || eq_txt.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill in all fields.", "Warning", JOptionPane.WARNING_MESSAGE);

            }else{
                HR_Manager hr = new HR_Manager();
                hr.updateSet(name_txt , add_txt ,eq_txt , passv);
                
                Hr_Manager_service hr_service = new Hr_Manager_service();
                boolean status = hr_service.updateUser(id, hr);
                if (status) {
                    view_table();
                    fun_desable_update_delete();
                    clearfun();
                   

                    JOptionPane.showMessageDialog(null, "HR Manager updated successfully", "Update Status", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to update HR Manager", "Update Status", JOptionPane.ERROR_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cancel updating.", "Cancel Update", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

